def get_data(raw_data):
    lines = raw_data.split("\n")
    data = [l.split() for l in lines if len(l.split()) == 3]
    data = [[float(d[0]), float(d[-1])] for d in data]
    return data


def get_fpp(E, data):
    epsilon = 0.1
    for i, d in enumerate(data):
        e = d[0]
        v = d[1]
        if abs(e - E) <= epsilon:
            return v
        elif E - e > epsilon:
            if i + 1 < len(data) and E < data[i + 1][0]:
                e1 = data[i + 1][0]
                v1 = data[i + 1][1]
                slope = (v1 - v) / (e1 - e)
                intercept = v - slope * e
                return slope * E + intercept  # ke + b
            elif i + 1 >= len(data):
                return data[-1][1]
    raise ValueError(f"Energy {E} eV is out of range for lookup tables.")


