def cal_silicon_CdTe_NIST(E, use_mu_en=True):
    """
    Calculate SILICON parameter for CdTe using NIST data.
    
    Args:
        E (float): Energy in eV. 
                   If < 100.0, assumed to be wavelength (Angstrom) and converted.
        use_mu_en (bool): If True, use mu_en/rho (energy absorption coefficient).
                          If False (default), use mu/rho (total mass attenuation).
                   
    Returns:
        float: Linear attenuation coefficient in mm^-1 (SILICON parameter).
    """
    # Handle input unit: eV vs Angstrom
    if E < 100.0:
        logger.warning(f"Input value {E} is likely wavelength (A), converting to Energy (eV)")
        E = 12398.0 / E
        
    energy_kev = E / 1000.0
    
    # Parse NIST data
    # Columns: Energy(keV), mu/rho(cm^2/g), mu_en/rho(cm^2/g)
    col_idx = 2 if use_mu_en else 1  # column index: 1=mu/rho, 2=mu_en/rho
    lines = CdTe_NIST.strip().split('\n')
    data = []
    for line in lines:
        parts = line.split()
        if len(parts) >= 3:
            try:
                e_val = float(parts[0])
                mu_val = float(parts[col_idx])
                data.append((e_val, mu_val))
            except ValueError:
                continue
    
    data.sort(key=lambda x: x[0])
    
    # Interpolate
    # Find surrounding points
    # Since energies span orders of magnitude, linear interpolation on log-log is physically better,
    # but linear-linear is likely sufficient for small ranges or if dense enough. 
    # Let's use simple linear interpolation on values for now, consistent with other helpers.
    
    mu_en_interp = None
    
    # Check bounds
    if energy_kev < data[0][0]:
        # logger.warning(f"Energy {energy_kev} keV below NIST data range. Extrapolating or using min.")
        # Simple linear extrapolation from first two points? Or just return min?
        # Let's linear extrapolate from first two points
        x0, y0 = data[0]
        x1, y1 = data[1]
        slope = (y1 - y0) / (x1 - x0)
        mu_en_interp = y0 + slope * (energy_kev - x0)
        
    elif energy_kev > data[-1][0]:
        # logger.warning(f"Energy {energy_kev} keV above NIST data range.")
         # Linear extrapolate from last two points
        x0, y0 = data[-2]
        x1, y1 = data[-1]
        slope = (y1 - y0) / (x1 - x0)
        mu_en_interp = y1 + slope * (energy_kev - x1)
        
    else:
        # Linear search
        for i in range(len(data) - 1):
            x0, y0 = data[i]
            x1, y1 = data[i+1]
            if x0 <= energy_kev <= x1:
                slope = (y1 - y0) / (x1 - x0)
                mu_en_interp = y0 + slope * (energy_kev - x0)
                break
    
    if mu_en_interp is None:
         # Should not happen given logic above
         return 0.0

    # Convert mass attenuation (cm^2/g) to linear attenuation (mm^-1)
    # mu_linear (cm^-1) = mu_mass (cm^2/g) * density (g/cm^3)
    # mu_linear (mm^-1) = mu_linear (cm^-1) / 10.0
    
    density_CdTe = 5.85 # g/cm^3
    mu_linear_cm = mu_en_interp * density_CdTe
    mu_linear_mm_inv = mu_linear_cm / 10.0
    
    return mu_linear_mm_inv


