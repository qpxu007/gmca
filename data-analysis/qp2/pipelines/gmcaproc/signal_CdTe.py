def silicon(E, fpp, atomic_mass, density):
    # https://strucbio.biologie.uni-konstanz.de/xdswiki/index.php/SILICON
    # fpp = f-double-prime (electron equivalents)
    # density = bulk density of material in g/cm^3
    # atomic_mass is in g/mol
    # E in eV
    r0 = 2.817941e-15  # meters
    wavelength = (12398.0 / E) / 1.0e10
    xlambda = wavelength  # meters
    N_Avogadro = 6.02214179e23  # molecules/mol
    logger.debug(f"== fpp={fpp} xlambda={xlambda} density={density} atomic_mass={atomic_mass}")
    SILICON = 2000.0 * r0 * xlambda * fpp * N_Avogadro * density / atomic_mass
    return SILICON


